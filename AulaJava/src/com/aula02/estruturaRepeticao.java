package com.aula02;

public class estruturaRepeticao {

	public static void main(String[] args) {

		//Laço de repetição while
		System.out.println("Laço While");
		int cont = 6;
		while(cont <= 5) {
			System.out.println(cont);
			cont++;
		}
		
		System.out.println("\nLaço Do While");
		int cont2 = 6;
		do {
			System.out.println(cont2);
			cont2++;
		}while(cont2 <=5 );
		
		
		
	}

}
